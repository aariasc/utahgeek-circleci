module.exports.add = function(num1, num2) {
  return (num1 + num2);
};

module.exports.substract = function(num1, num2) {
  return (num1 - num2);
};

module.exports.multiply = function(num1, num2) {
  return (num1 * num2);
};

module.exports.divide = function(num1, num2) {
  return (num1 / num2);
};
